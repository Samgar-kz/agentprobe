#!/usr/bin/env python3
"""Generate the README results tables directly from data/*.csv.

Single source of truth: the numbers in the README must equal what the CSVs say.
This script computes per-defense leak rate (injection) and success rate (utility)
with Wilson 95% CIs, renders them as markdown, and can rewrite the README blocks
between AUTOGEN markers — or verify they are in sync (for CI).

Usage:
    python scripts/gen_results_tables.py            # print tables to stdout
    python scripts/gen_results_tables.py --write    # update README between markers
    python scripts/gen_results_tables.py --check     # exit 1 if README is stale

CI guard: `--check` is what enforces CONTRIBUTING's "no fabricated results" rule —
every number in the README traces to a committed CSV row.
"""

from __future__ import annotations

import csv
import sys
from collections import defaultdict
from pathlib import Path

# Import the same Wilson CI used by the harness so tables match the tool exactly.
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
from agentprobe.metrics import wilson_ci  # noqa: E402

ROOT = Path(__file__).resolve().parent.parent
DATA = ROOT / "data"
README = ROOT / "README.md"

# Canonical defense order for display (any extras are appended alphabetically).
DEFENSE_ORDER = ["none", "delimited", "spotlight", "sandwich", "instr_hierarchy", "llm_filter"]

# Injection CSVs to summarize, in display order. Only files that exist are
# rendered, so adding a provider is just dropping its CSV in data/ and listing it.
INJECTION_CSVS = [
    ("gpt-4o-mini", "gpt4omini.csv"),
    ("gpt-4o", "gpt4o.csv"),
    ("claude-haiku-4-5", "haiku45.csv"),
    ("gemini-2.5-flash", "gemini.csv"),
    ("deepseek-chat", "deepseek.csv"),
]
UTILITY_CSV = ("gpt-4o-mini", "utility_gpt4omini.csv")

BEGIN = "<!-- AUTOGEN:results BEGIN (generated by scripts/gen_results_tables.py — do not edit by hand) -->"
END = "<!-- AUTOGEN:results END -->"


def _ordered(names: set[str]) -> list[str]:
    known = [d for d in DEFENSE_ORDER if d in names]
    extra = sorted(n for n in names if n not in DEFENSE_ORDER)
    return known + extra


def _read_counts(path: Path, value_col: str, positive: str | int) -> dict[str, list[int]]:
    """Return {defense: [positives, total]} from a CSV.

    For injection CSVs, positive==1 in the `leaked` column.
    For utility CSVs, positive=="SUCCESS" in the `outcome` column.
    """
    counts: dict[str, list[int]] = defaultdict(lambda: [0, 0])
    with open(path, newline="") as f:
        for row in csv.DictReader(f):
            defense = row["defense"]
            raw = row.get(value_col, "")
            if raw == "":
                continue  # skip error rows
            counts[defense][1] += 1
            if str(raw) == str(positive):
                counts[defense][0] += 1
    return counts


def _injection_table(model: str, path: Path) -> str:
    counts = _read_counts(path, "leaked", 1)
    lines = [f"**{model}**", "", "| Defense | Leak Rate | 95% CI | N |", "|---|---|---|---|"]
    for d in _ordered(set(counts)):
        leaks, n = counts[d]
        p, lo, hi = wilson_ci(leaks, n)
        lines.append(f"| `{d}` | {p*100:.1f}% | {lo*100:.1f}–{hi*100:.1f}% | {n} |")
    return "\n".join(lines)


def _utility_table(model: str, path: Path) -> str:
    counts = _read_counts(path, "outcome", "SUCCESS")
    lines = [
        f"**{model} — utility (benign-task success)**", "",
        "| Defense | Success Rate | 95% CI | N |", "|---|---|---|---|",
    ]
    for d in _ordered(set(counts)):
        ok, n = counts[d]
        p, lo, hi = wilson_ci(ok, n)
        lines.append(f"| `{d}` | {p*100:.1f}% | {lo*100:.1f}–{hi*100:.1f}% | {n} |")
    return "\n".join(lines)


def render() -> str:
    blocks = []
    for model, fname in INJECTION_CSVS:
        path = DATA / fname
        if path.exists():
            blocks.append(_injection_table(model, path))
    upath = DATA / UTILITY_CSV[1]
    if upath.exists():
        blocks.append(_utility_table(UTILITY_CSV[0], upath))
    return "\n\n".join(blocks)


def _wrapped() -> str:
    return f"{BEGIN}\n\n{render()}\n\n{END}"


def main() -> int:
    mode = sys.argv[1] if len(sys.argv) > 1 else "--print"
    tables = render()

    if mode == "--print":
        print(tables)
        return 0

    text = README.read_text()
    block = _wrapped()

    if BEGIN in text and END in text:
        pre = text[: text.index(BEGIN)]
        post = text[text.index(END) + len(END):]
        new_text = pre + block + post
    else:
        # First run: append the block at the end so markers exist for next time.
        new_text = text.rstrip() + "\n\n## Results (auto-generated)\n\n" + block + "\n"

    if mode == "--check":
        if new_text != text:
            print("README results tables are STALE. Run: python scripts/gen_results_tables.py --write")
            return 1
        print("README results tables are in sync with data/.")
        return 0

    if mode == "--write":
        README.write_text(new_text)
        print(f"Updated {README} results tables from data/.")
        return 0

    print(f"Unknown mode: {mode}. Use --print | --write | --check.")
    return 2


if __name__ == "__main__":
    raise SystemExit(main())
